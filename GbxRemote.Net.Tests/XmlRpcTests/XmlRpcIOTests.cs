        [Fact]
        public void Reads_One_Byte_Correctly() {
            MemoryStream stream = new(new byte[] { 0xa4 });
            XmlRpcIO io = new(stream);

            byte[] read = io.ReadBytesAsync(1).GetAwaiter().GetResult();

            Assert.Equal(0xa4, read[0]);
